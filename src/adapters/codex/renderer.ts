function createSkillFiles(
  resolveResult: ResolveResult,
  generatedDir: string,
): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  for (const skill of resolveResult.available) {
    if (skill.kind !== 'skill') {
      continue;
    }

    const skillDir = getSkillOutputDirName(skill);

    if (!skillDir) {
      continue;
    }

    const outputDir = normalizeRelativePath(join(generatedDir, skillsDirName, skillDir));
    files.push({
      relativePath: normalizeRelativePath(join(outputDir, 'SKILL.md')),
      content: renderCodexSkill(skill, skillDir),
    });

    for (const resourceFile of skill.resourceFiles || []) {
      if (!isSafeSkillRelativePath(resourceFile.relativePath)) {
        continue;
      }

      files.push({
        relativePath: normalizeRelativePath(join(outputDir, resourceFile.relativePath)),
        content: normalizeNewlines(resourceFile.content),
      });
    }
  }

  return files.sort((left, right) => left.relativePath.localeCompare(right.relativePath));
}

function renderCodexSkill(skill: AvailableSkill, name: string): string {
  return normalizeNewlines(`---
name: ${name}
description: ${JSON.stringify(skill.summary)}
---

${skill.body.trim()}
`);
}

function getGeneratedSkillEntrypoint(
  skillFiles: GeneratedFile[],
  skill: AvailableSkill,
): string | undefined {
  const skillDir = getSkillOutputDirName(skill);

  if (!skillDir) {
    return undefined;
  }

  const directSuffix = '/skills/' + skillDir + '/SKILL.md';
  const namespacedSuffix = '/skills/agentsync-' + skillDir + '/SKILL.md';

  return skillFiles.find((file) => (
    file.relativePath.endsWith(directSuffix) ||
    file.relativePath.endsWith(namespacedSuffix)
  ))?.relativePath;
}

function getSkillOutputDirName(skill: AvailableSkill): string | undefined {
  const match = skill.entrypoint?.match(/^skills\/(.+)\/material\.md$/);

  if (!match) {
    return undefined;
  }

  return match[1];
}

function isSafeSkillRelativePath(relativePath: string): boolean {
  return (
    normalizeRelativePath(relativePath) === relativePath &&
    relativePath !== '' &&
    !relativePath.startsWith('/') &&
    !relativePath.startsWith('../') &&
    !relativePath.includes('/../')
  );
}

function createRuleFiles(
  resolveResult: ResolveResult,
  generatedDir: string,
): GeneratedFile[] {
  const groups = new Map<string, ResolvedSkill[]>();

  for (const skill of resolveResult.included) {
    const current = groups.get(skill.category) || [];
    current.push(skill);
    groups.set(skill.category, current);
  }

  const requiredSkills = resolveResult.included
    .filter((skill) => (
      skill.severity === 'required' &&
      bootstrapRequiredCategories.includes(skill.category)
    ));
  const files: GeneratedFile[] = [];

  files.push({
    relativePath: normalizeRelativePath(join(generatedDir, rulesDirName, 'required.md')),
    content: renderRuleFile('Обязательные правила', requiredSkills),
  });

  for (const category of Array.from(groups.keys()).sort()) {
    files.push({
      relativePath: normalizeRelativePath(join(generatedDir, rulesDirName, `${category}.md`)),
      content: renderRuleFile(categoryTitles[category] || category, groups.get(category) || []),
    });
  }

  return files;
}

function renderRuleFile(title: string, skills: ResolvedSkill[]): string {
  const sections = skills
    .map((skill) => renderSkillSection(skill))
    .join('\n\n');

  return normalizeNewlines(`# ${title}

${sections || 'Нет правил для этой категории в текущем контексте.'}
`);
}

function renderSkillSection(skill: ResolvedSkill): string {
  return `## ${skill.title}

Metadata: \`${skill.id}@${skill.version}\`, category: \`${skill.category}\`, severity: \`${skill.severity}\`

${skill.body.trim()}`;
}

function createCategoryLinks(ruleFiles: GeneratedFile[]): Record<string, string> {
  const result: Record<string, string> = {};

  for (const file of ruleFiles) {
    const match = file.relativePath.match(/rules\/(.+)\.md$/);

    if (!match) {
      continue;
    }

    const category = match[1];
    result[category] = `[${category}](rules/${category}.md)`;
  }

  return result;
}

function getRuleFileTitle(relativePath: string): string {
  const match = relativePath.match(/rules\/(.+)\.md$/);

  if (!match) {
    return relativePath;
  }

  const category = match[1];

  if (category === 'required') {
    return 'Bootstrap required';
  }

  return categoryTitles[category] || category;
}

function getGeneratedRelativeLink(relativePath: string): string {
  const match = relativePath.match(/\.agents\/generated\/(.+)$/);
  return match ? match[1] : relativePath;
}

function normalizeNewlines(value: string): string {
  return `${value.replace(/\r\n/g, '\n').trim()}\n`;
}

function normalizeRelativePath(value: string): string {
  return value.replace(/\\/g, '/');
}
