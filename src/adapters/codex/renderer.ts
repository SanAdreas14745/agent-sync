import { join } from 'node:path';
import {
  AvailableSkill,
  ResolveResult,
  ResolvedSkill,
} from '../../composer/types';
import {
  CodexAdapterInput,
  CodexGeneratedFiles,
  GeneratedFile,
} from './types';

const activeRulesFileName = 'active-rules.md';
const ruleScopeFileName = 'rule-scope.md';
const skillIndexFileName = 'skill-index.json';
const rulesDirName = 'rules';
const skillsDirName = 'skills';
const bootstrapRequiredCategories = [
  'communication',
  'git',
  'general',
  'verification',
  'dependencies',
  'security',
];

const categoryTitles: Record<string, string> = {
  general: 'Общие правила',
  communication: 'Коммуникация',
  git: 'Git и рабочая директория',
  verification: 'Проверки',
  dependencies: 'Зависимости',
  frontend: 'Frontend',
  bff: 'BFF',
  backend: 'Backend',
  typescript: 'TypeScript',
  html: 'HTML templates',
  scss: 'SCSS',
  architecture: 'Архитектура',
  security: 'Безопасность',
};

export function generateCodexFiles(input: CodexAdapterInput): CodexGeneratedFiles {
  const activeRulesPath = normalizeRelativePath(join(input.output.generatedDir, activeRulesFileName));
  const ruleScopePath = normalizeRelativePath(join(input.output.generatedDir, ruleScopeFileName));
  const skillIndexPath = normalizeRelativePath(join(input.output.generatedDir, skillIndexFileName));
  const ruleFiles = createRuleFiles(input.resolveResult, input.output.generatedDir);
  const skillFiles = createSkillFiles(input.resolveResult, input.output.generatedDir);

  const entry: GeneratedFile = {
    relativePath: normalizeRelativePath(input.output.entry),
    content: renderAgentsMd(activeRulesPath, ruleScopePath, skillIndexPath),
  };
  const activeRules: GeneratedFile = {
    relativePath: activeRulesPath,
    content: renderActiveRules(input.resolveResult, ruleScopeFileName, ruleFiles),
  };
  const ruleScope: GeneratedFile = {
    relativePath: ruleScopePath,
    content: renderRuleScope(input.resolveResult, ruleFiles),
  };
  const skillIndex: GeneratedFile = {
    relativePath: skillIndexPath,
    content: renderSkillIndex(input.resolveResult, ruleFiles, skillFiles),
  };

  return {
    entry,
    activeRules,
    ruleScope,
    skillIndex,
    ruleFiles,
    skillFiles,
    files: [entry, activeRules, ruleScope, skillIndex, ...ruleFiles, ...skillFiles],
  };
}

export function renderAgentsMd(
  activeRulesPath: string,
  ruleScopePath: string,
  skillIndexPath: string,
): string {
  return normalizeNewlines(`# Инструкции для AI-агента

В этом репозитории используются централизованные AI-скиллы.

Всегда сначала прочитай bootstrap-правила:
- [${activeRulesPath}](${activeRulesPath})

Для выбора правил под конкретную задачу используй карту применимости:
- [${ruleScopePath}](${ruleScopePath})

Индекс всех сгенерированных правил и справочных материалов:
- [${skillIndexPath}](${skillIndexPath})

Generated local skills:
- [.agents/generated/skills](.agents/generated/skills)

Файлы сгенерированы автоматически. Не редактируй их вручную.
Для обновления запусти \`ai-skills sync\`.
`);
}

export function renderActiveRules(
  resolveResult: ResolveResult,
  ruleScopePath: string,
  ruleFiles: GeneratedFile[],
): string {
  const manifest = resolveResult.manifest;
  const includedSkills = manifest.included
    .map((skill) => `- ${skill.id}@${skill.version}`)
    .join('\n');
  const taskTypeLine = manifest.taskType ? `Task type: ${manifest.taskType}\n` : '';
  const availableRuleSets = ruleFiles
    .map((file) => {
      const title = getRuleFileTitle(file.relativePath);
      const link = getGeneratedRelativeLink(file.relativePath);
      return `- [${title}](${link})`;
    })
    .join('\n');

  return normalizeNewlines(`<!--
Generated by ai-skills
Project: ${manifest.project}
Agent: ${manifest.agent}
${taskTypeLine}Estimated tokens: ${manifest.estimatedTokens}
Included rules:
${includedSkills || '- none'}
Checksum: ${manifest.checksum}
-->

# Active AI Rules

Этот файл является коротким bootstrap-слоем. Не пытайся применить все
правила сразу: сначала определи область задачи, затем открой релевантные
разделы из карты применимости.

## Доступные активные наборы правил

${availableRuleSets || '- Нет активных наборов правил для текущего контекста.'}

## Как выбирать правила

1. Определи область задачи по изменяемым путям, технологиям и типу задачи.
2. Открой карту применимости: [${ruleScopePath}](${ruleScopePath}).
3. Примени обязательные правила и только релевантные category rules.
4. Не усложняй маленькую правку нерелевантными правилами.
5. Если задача затрагивает несколько областей, применяй правила всех затронутых областей.
`);
}

